                thumb_index_distance = np.linalg.norm(np.array([thumb_x, thumb_y]) - np.array([index_x, index_y]))
                # 엄지손가락과 중지손가락의 거리 계산
                thumb_middle_distance = np.linalg.norm(np.array([thumb_x, thumb_y]) - np.array([middle_x, middle_y]))
